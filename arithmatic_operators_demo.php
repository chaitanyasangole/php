<?php
/*

There are following operators :-
+ for addition
- for substraction
* for multiplication
/ for division
** for exponentiation 

*/
$num1 = 10;
$num2 = 2;

$add = $num1 + $num2;
$sub = $num1 - $num2;
$mul = $num1 * $num2;
$div = $num1 / $num2;
$exp = $num1 ** $num2;
echo 'num1 =' . $num1 . ', num2=' . $num2 . ' <br>';
echo 'Addition of two numbers=' . $add . ' <br>';
echo 'Substraction of two numbers=' . $sub . ' <br>';
echo 'Multiplication of two numbers=' . $mul . ' <br>';
echo 'Division of two numbers=' . $div . ' <br>';
echo 'Exponentiation of two numbers=' . $exp . ' <br>';
