<?php
$day = 'monday';
switch ($day) {
    case 'monday':
        echo 'It is monday';
        break;

    case 'tuesday':
        echo 'It is tuesday';
        break;

    case 'wednesday':
        echo 'It is wednesday';
        break;

    case 'thursday':
        echo 'It is thursday';
        break;

    case 'friday':
        echo 'It is friday';
        break;

    case 'saturday':
        echo 'It is saturday';
        break;

    case 'sunday':
        echo 'It is sunday';
        break;

    default:
        echo 'Invalid day';
        break;
}
